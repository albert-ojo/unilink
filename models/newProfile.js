class NewProfile {
  constructor(
    id,
    name,
    university,
    degree,
    year,
    skills,
    picture,
    email,
    cellNo,
    userId
  ) {
    this.id = id;
    this.name = name;
    this.university = university;
    this.degree = degree;
    this.year = year;
    this.skills = skills;
    this.picture = picture;
    this.email = email;
    this.cellNo = cellNo;
    this.userId = userId;
  }
}
export default NewProfile;
