class NewInterest {
  constructor(
    id,
    userName,
    projectName,
    userMessage,
    userId,
    projectId,
    postedUserId,
    status
  ) {
    this.id = id;
    this.userName = userName;
    this.projectName = projectName;
    this.userMessage = userMessage;
    this.userId = userId;
    this.projectId = projectId;
    this.postedUserId = postedUserId;
    this.status = status;
  }
}

export default NewInterest;
